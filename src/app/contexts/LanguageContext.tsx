export function LanguageProvider({ children }: { children: React.ReactNode }) {
  const [language, setLanguage] = useState<Language>('zh');
  const [mounted, setMounted] = useState(false);

  useEffect(() => {
    setMounted(true);
    // Only access localStorage after component mounts
    if (typeof window !== 'undefined') {
      const savedLanguage = localStorage.getItem('language') as Language;
      if (savedLanguage && (savedLanguage === 'zh' || savedLanguage === 'en')) {
        setLanguage(savedLanguage);
      }
    }
  }, []);

  useEffect(() => {
    // Only save to localStorage after component is mounted and on client side
    if (mounted && typeof window !== 'undefined') {
      localStorage.setItem('language', language);
    }
  }, [language, mounted]);

  const toggleLanguage = () => {
    setLanguage(prev => prev === 'zh' ? 'en' : 'zh');
  };

  const t = (key: string): string => {
    try {
      const langTable: any = (translations as any)[language];
      if (langTable && typeof langTable === 'object' && key in langTable && langTable[key]) {
        return langTable[key];
      }
      // Fallback to zh, then en
      if ((translations as any).zh?.[key]) return (translations as any).zh[key];
      if ((translations as any).en?.[key]) return (translations as any).en[key];
      return key;
    } catch {
      return key;
    }
  };

  const formatDate = (date: string): string => {
    if (!date) return '';
    const [year, month, day] = date.split('-');
    if (language === 'zh') {
      return `${year}年${month}月${day}日`;
    } else {
      return `${month}/${day}/${year}`;
    }
  };

  return (
    <LanguageContext.Provider value={{ language, toggleLanguage, t, formatDate }}>
      {children}
    </LanguageContext.Provider>
  );
}

export function useLanguage() {
  const context = useContext(LanguageContext);
  if (context === undefined) {
    throw new Error('useLanguage must be used within a LanguageProvider');
  }
  return context;
}